# Crie um programa que leia dois números inteiros e mostre a soma entre eles.

# usuário atribui os dados para cada variável
numero1 = int(input('Digite um número: '))
numero2 = int(input('Digite outro número: '))

# somatório entre as variáveis.
soma = numero1 + numero2

# exibição do resultado na tela.
print(f'O resultado da soma entre os números {numero1} e {numero2} é {soma}.')
